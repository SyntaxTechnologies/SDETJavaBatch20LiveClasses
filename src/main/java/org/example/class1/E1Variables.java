package org.example.class1;

public class E1Variables {
    public static void main(String[] args) {
        //Common ways of creating variables
        //Creating variable but not assigning any value
        int age;
        //Creating variable an assigning value as well on the same line
        String name="Ali";
        age=20;// we are storing the number 20 inside the age variable
        System.out.println(age);
        System.out.println(name);
    }
}
