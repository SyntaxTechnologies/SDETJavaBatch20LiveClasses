package org.example.class2;

public class E7rithmeticOp {
    public static void main(String[] args) {

        int num=10;
        int result=20;

        System.out.println(result);
        result+=num; //30
        System.out.println(result);
        result-=20; //30-20=>10
        System.out.println(result);


    }
}
